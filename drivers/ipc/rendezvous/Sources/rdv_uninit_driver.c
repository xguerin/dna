#include <Private/Driver.h>
#include <MemoryManager/MemoryManager.h>

void rdv_uninit_driver (void)
{
  for (int32_t i = 0; i < CHANNEL_RDV_NDEV; i++)
  {
    kernel_free ((void *)rdv_devices[i]);
  }

  kernel_free (rdv_devices);
}

